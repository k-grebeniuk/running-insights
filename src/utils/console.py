def page_progress(current: int, total: int) -> None:
    """
    Обновляет строку прогресса при обработке страниц результатов.

    Используется вместо вывода отдельного сообщения для каждой страницы,
    чтобы при больших мероприятиях с сотнями страниц не перегружать
    терминал большим количеством однотипных сообщений.

    Args:
        current (int):
            Номер текущей обрабатываемой страницы.

        total (int):
            Общее количество страниц.

    Returns:
        None
    """
    print(
        f"\r  ▶ Страница {current}/{total}",
        end="",
        flush=True,
    )


